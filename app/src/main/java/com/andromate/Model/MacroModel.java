package com.andromate.Model;

public class MacroModel {

    String macro_state;
    String active_time;
    String macro_name;
    String macro_des;


    public String getMacro_state() {
        return macro_state;
    }

    public void setMacro_state(String macro_state) {
        this.macro_state = macro_state;
    }

    public String getActive_time() {
        return active_time;
    }

    public void setActive_time(String active_time) {
        this.active_time = active_time;
    }

    public String getMacro_name() {
        return macro_name;
    }

    public void setMacro_name(String macro_name) {
        this.macro_name = macro_name;
    }

    public String getMacro_des() {
        return macro_des;
    }

    public void setMacro_des(String macro_des) {
        this.macro_des = macro_des;
    }
}
